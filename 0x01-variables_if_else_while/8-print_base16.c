#include <stdio.h>
/**
 * main - Entry point
 * description: 'all single digit numbers of base 10 starting from 0'
 * Return: always 0
 */

int main(void)
{
		int n;
		int c;

		for (n = 48; n <= 57; n++)
		{
			putchar(n);
		}
		for (c = 97; c <= 102; n++)
		{
			putchar(c);
		}
		putchar('\n');
		return (0);
}
